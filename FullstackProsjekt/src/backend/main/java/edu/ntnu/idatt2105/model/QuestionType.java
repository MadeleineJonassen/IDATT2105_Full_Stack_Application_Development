package edu.ntnu.idatt2105.model;

/**
 * Enum representing different types of questions.
 */
public enum QuestionType {
    /**
     * Represents a question with a string answer.
     */
    STRING,

    /**
     * Represents a true or false question.
     */
    TRUE_OR_FALSE,

    /**
     * Represents a multiple choice question.
     */
    MULTIPLE_CHOICE
}
